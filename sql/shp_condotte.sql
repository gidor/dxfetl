SELECT 
 INI.X AS "X",
 INI.Y AS "Y",
 FIN.X AS "X1",
 FIN.Y AS "Y1",
 ((INI.Z - (INI.FONTUBO / 100)) - (FIN.Z - (FIN.FONTUBO / 100))) as "DH",
 'C070201' as COD_CLASSE,
 0 as FILE_ID,
 INI.`DATA` as RILIEVO,
 INI.ISTAT_ID as COM_ISTAT,
'91' as TP_STR_COD,
'91' as TP_STR_NOM,
'91' as ES_AMM_CF,
'91' as L_EG_COD,
'UNIACQUE' as L_EG_NOM,
null as L_BORN,
INI.ALT_LIN  as L_DIA,
0 as L_LUNG,
 INI.MATLIN as L_MAT,
'91' as L_STA,
'91' as L_PRO,
 INI.POS_TRATTA as L_POS,
 INI.POS_SUP as L_POS_SUP,
'91' as L_INFR_TY,
 INI.OBJECTID  as NODO_INI,
 FIN.OBJECTID as NODO_FIN,
'91' as L_F_TY,
'91' as L_F_TIPFOG,
 INI.TIPLINIDR as L_F_TIPLIN,
 INI.FORMSEZ as L_F_FORSEZ,
 INI.ALT_LIN  as L_F_ALT,
 INI.LARGH_LIN as L_F_LARG
FROM (((TUBI AS INI) 
 	INNER JOIN TUBI_REL AS SE ON SE.`INI` = INI.`ID`)
 	INNER JOIN  TUBI AS FIN ON  SE.`FIN` = FIN.`ID` )
; 
